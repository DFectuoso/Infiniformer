// Reset the camara to the left of the screen
// This is called at the beginning and also on die
void LevelMap::resetCamera(){
  x = 0;
  autoScrollDelay = 0;
  y = HEIGHT/2 - TILEH/2;   
}

// Init the map, which means creating a bitmap, loading the tiles and drawing the first map
void LevelMap::init(){
    // create a bitmap, and load the tiles
    scroll = create_bitmap(MAPW, MAPH);
    if (scroll == NULL) {
        set_gfx_mode(GFX_TEXT, 0, 0, 0, 0);
        allegro_message("Error creating virtual background");
        return;
    }

    tiles = load_bitmap("tiles.bmp", NULL);
    if (tiles == NULL) {
        set_gfx_mode(GFX_TEXT, 0, 0, 0, 0);
        allegro_message("Error loading tiles.bmp file");
        return;
    }

    // copy the first map to the virtual map
    for (tiley=0; tiley < scroll->h; tiley+=TILEH) {
      for (tilex=0; tilex < scroll->w; tilex+=TILEW) {
        map[tiley/TILEH][tilex/TILEW] = map1[tiley/TILEH][tilex/TILEW];
      }
    }

    // Draw the first map into the scroll bitmap
    for (tiley=0; tiley < scroll->h; tiley+=TILEH) {
        for (tilex=0; tilex < scroll->w; tilex+=TILEW) {
            blit(grabframe(tiles, TILEW, TILEH, 0, 0, COLS, (map[tiley/TILEH][tilex/TILEW]-1)), scroll, 0, 0, tilex, tiley, TILEW, TILEH);
        }
    }
}

