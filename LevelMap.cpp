bool LevelMap::die(int playerX, int playerY, int width, int height){
  if(playerY > MAPH - height){
    return true;
  }
  if (map[(playerY + height) / TILEH][(playerX + 15)/ TILEW] == 26 || map[(playerY + height) / TILEH][((playerX + 45) / TILEW)] == 26){
    return true;
  }  else{
    return false;
  }  
}

// Nice funtion to take a frame from the tilemap and paste it on the source
BITMAP* LevelMap::grabframe(BITMAP *source, int width, int height, int startx, int starty, int columns, int frame){
    BITMAP *temp = create_bitmap(width,height);
    int x = startx + (frame % columns) * width;
    int y = starty + (frame / columns) * height;
    blit(source,temp,x,y,0,0,width,height);
    return temp;
}

