//The player moved up, should the camara move?
void LevelMap::up(int playerY){
  if(playerY - y < HEIGHT/2){
    y -= STEP;
  }
}

//The player moved down, should the camara move?
void LevelMap::down(int playerY){
  if(playerY - y > HEIGHT/2){
    y += STEP;
  }
}

